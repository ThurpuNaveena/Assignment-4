import numpy as np
import matplotlib.pyplot as plt
 
x = np.linspace(-3.9,4.6,100)
p=np.poly1d([3,-2,1/3])
for i in range(len(x)):
    y=p(x)
 
plt.plot(x,y)
plt.xlabel('$x$')
plt.ylabel('$y$')
plt.legend(loc='best')
plt.grid() # minor
plt.axis('equal')
plt.show()